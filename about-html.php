                            <h3>Allgemeine Informationen über das Internet</h3>
                            <br /><br />
                            <span id="text-headline-about">Das Internet:</span><br /><br />
                            <p>
                            ➔ -riesiges Netzwerk von miteinander vernetzten Computer und Servern<br />
                            ➔ -es entstand bei der Suche nach einem atom sicheren Datennetz<br />
                            ➔ -bei der Datenübertragung werden viele einzelne Pakete verschickt, die dann
                            beim Empfänger zusammengebaut werden<br />
                            ➔ Übertragungsverfahren sind das “IP” (Internet Protocol) und das TCP
                            (Transmission Control Protocol). <br />Diese dienen zur Übertragung der oben
                            genannten “Pakete”.
                            <br /><br />
                            <span id="text-headline-about">Das World Wide Web:</span><br /><br />
                            ➔ ist nur ein Teil vom Internet (andere Teile sind z.B. das Darknet / Deepweb)
                            und wurde 1989/90<br /> von Kernphysikern zum Austausch von Bildern, Ton usw.
                            entwickelt<br />
                            ➔ prägend war die Entwicklung der HTML Sprache (Hypertext Markup
                            Language), welche den Seiteninhalt logisch<br /> beschreibt. Entwickler war Tim
                            Bernes Lee, welcher die erste Homepage im WWW programmierte.</p>
