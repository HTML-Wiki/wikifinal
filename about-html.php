                            <h3>Allgemeine Informationen über den Aufbau & die Funktionsweise von Webseiten</h3><br /><br />
                            <p><span id="text-headline-about">Domain Name System (DNS):</span><br /><br />
                            • Jeder am Internet angeschlossene Rechner hat weltweit eindeutige Adresse<br />
                            🡪 IP-Adresse: z.B.: 127.145.5.49
                            • Adressen zentral in Kalifornien verwaltet 🡪 komplizierte
                            Zahlenfolgen(IP-Adressen) <br />🡪 DNS (wandelt Zahlen in einen Text um =>
                            URL) 🡪 Rechnern werden symbolische Namen zugewiesen z.B.:<br />
                            http://www.zuseum.de oder https://google.de statt 127.86.278.61<br /><br />
                            Im World Wide Web können Webseiten durch eine Internetadresse, die URL,
                            erreicht werden. <br />Diese hat verschiedene Endungen, die wieder für andere Begriffe
                            stehen. So gibt es z.B.<br /><br />
                            ➔ .de = Länderkennung (Deutschland)<br />
                            ➔ .com = Commerzieler Charakter<br />
                            ➔ .edu = Education (Bildungseinrichtung) 
                            usw...<br /><br />
                            Im Internet gibt es Web-Server: ist direkt am Web angeschlossen = Diener im Netz 🡪 <br />hier liegen
                            die Webseiten 🡪 eigener Rechner greift auf den Webserver zu<br />
                            • auf Client ist ein Programm zum Betrachten der Webseiten = Browser (z.B.
                            Opera, Chrome, Firefox)<br />
                            • HTTP 🡪 Bote(Dienst im Internet), der die Seiten vom Server zum Client überträgt<br /><br />
                                        </p>
