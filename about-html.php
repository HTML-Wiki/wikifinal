                                            <h3>Allgemeine Informationen über HTML</h3>
                                            <p>
                                                Sprache des WWW 🡪 HTML:<br />
                                                • Code zur logischen Beschreibung von Dokumenten, in englischer Sprache<br />
                                                • Weniger Layout des Dokumentes dargestellt, vielmehr die Struktur:
                                                Hauptüberschrift – Unterüberschrift – dort fetter Text – da Aufzählung usw.<br />
                                                • HTML beruht auf ASCII ( amerikanischer Standardcode zum
                                                Informationsaustausch)<br />
                                                • ASCII kann nur Text darstellen<br /><br />
                                                Formatierungen nur durch HTML-Befehle, die Tags, schreiben Textstruktur
                                                vor (Siehe Menüeintrag "Tag's"<br />
                                                • Befehle in spitzen Klammern 🡪 Befehl zum Einschalten einer Eigenschaft
                                                und Ausschalten<br />
                                                • HTML-Dateien sind einfache Textdateien mit der Endung htm bzw. html<br />
                                                • Um HTML Dateien zu erstellen und zu bearbeiten kann man verschiedene
                                                Programme, wie z.B den einfachen Editor,<br /> oder das Programm Visual Studio
                                                Code von Microsoft verwenden, welches extra auf das programmieren
                                                ausgelegt ist.</p><br /><br />
